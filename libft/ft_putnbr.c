#include "libft.h"

void	ft_putnbr(int n)
{
	char	digits[12];
	int		i;

	i = 0;
	if (n == 0)
		ft_putchar('0');
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		n = -n;
		ft_putchar('-');
	}
	while (n > 0)
	{
		digits[i++] = n % 10 + '0';
		n = n / 10;
	}
	while (i--)
		ft_putchar(digits[i]);
}
