#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char		*str1;
	const char	*str2;

	str1 = s1;
	str2 = s2;
	while (*str1)
		str1++;
	while (*str2)
		*str1++ = (char)*str2++;
	*str1 = '\0';
	return (s1);
}
